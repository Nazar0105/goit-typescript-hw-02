// Для переписування цього JavaScript масиву в TypeScript використовуючи концепцію
// кортежу, я можу визначити тип кортежу з вказаннями типів для кожного елемента.

// Ось як це може виглядати:

let person: [string, number] = ['Max', 21];

// Це означає, що "person" - це кортеж із двох елементів, де перший елемент є рядком, а другий - числом.


